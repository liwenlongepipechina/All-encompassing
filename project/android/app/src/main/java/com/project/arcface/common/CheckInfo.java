package com.project.arcface.common;

public class CheckInfo {
    /**
     * 判断是否注册成功
     */
    private Boolean register;
    /**
     * 判断是否识别成功
     */
    private Boolean recogniez;


    public Boolean getRegister() {
        return register;
    }

    public void setRegister(Boolean register) {
        this.register = register;
    }

    public Boolean getRecogniez() {
        return recogniez;
    }

    public void setRecogniez(Boolean recogniez) {
        this.recogniez = recogniez;
    }
}
